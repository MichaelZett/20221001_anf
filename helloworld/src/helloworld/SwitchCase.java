package helloworld;

public class SwitchCase {

	public static void main(String[] args) {
		int zahl = 1;
		switch (zahl) {
		case 1:
			System.out.println("Fall 1");
			break;
		case 2:
			System.out.println("Fall 2");
			break;
		case 3:
			System.out.println("Fall 3");
			break;
		default:
			System.out.println("Default Fall");
			break;
		}
	}

}
